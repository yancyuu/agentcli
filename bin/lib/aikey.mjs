// --- Provider → SDK env-var map (aikey commands_account.rs:1491) -------------
// Canonical code → [apiKeyVar, baseUrlVar]. Brand aliases resolve via
// canonicalProvider() before lookup.
const PROVIDER_ENV_VARS = {
  anthropic: ['ANTHROPIC_API_KEY', 'ANTHROPIC_BASE_URL'],
  openai: ['OPENAI_API_KEY', 'OPENAI_BASE_URL'],
  google: ['GOOGLE_API_KEY', 'GOOGLE_BASE_URL'],
  kimi: ['KIMI_API_KEY', 'KIMI_BASE_URL'],
  deepseek: ['DEEPSEEK_API_KEY', 'DEEPSEEK_BASE_URL'],
  moonshot: ['MOONSHOT_API_KEY', 'MOONSHOT_BASE_URL'],
};

// Brand aliases servers/users may send → canonical provider code (aikey accepts
// "claude"/"gpt"/"chatgpt"/"gemini" alongside the canonical names).
export function canonicalProvider(code) {
  const c = String(code || '').trim().toLowerCase();
  if (!c) return null;
  if (c === 'anthropic' || c === 'claude') return 'anthropic';
  if (c === 'openai' || c === 'gpt' || c === 'chatgpt') return 'openai';
  if (c === 'google' || c === 'gemini') return 'google';
  if (Object.prototype.hasOwnProperty.call(PROVIDER_ENV_VARS, c)) return c;
  return null;
}

export function providerEnvVars(code) {
  const canonical = canonicalProvider(code);
  if (!canonical) return null;
  return PROVIDER_ENV_VARS[canonical];
}

// --- renderActiveEnv (aikey write_active_env:1782) ---------------------------
// Pure: builds the ~/.hermit/aikey.env body. Writes the REAL key (no proxy here),
// and *_BASE_URL only when baseUrl is present (mirrors aikey handle_run_direct).
export function renderActiveEnv(bundle) {
  const displayName = bundle.displayName || 'agentcli';
  const lines = [
    '# openhermit active key — auto-generated by \'agentcli aikey\', do not edit manually',
    `export OPENHERMIT_ACTIVE_KEY="${displayName}"`,
  ];
  for (const [rawCode, creds] of Object.entries(bundle.providers || {})) {
    const apiKey = creds && typeof creds === 'object' ? String(creds.apiKey || '').trim() : '';
    if (!apiKey) continue;
    const envVars = providerEnvVars(rawCode);
    if (!envVars) continue;
    const [apiKeyVar, baseUrlVar] = envVars;
    lines.push(`export ${apiKeyVar}="${apiKey}"`);
    const baseUrl = String(creds.baseUrl || '').trim();
    if (baseUrl) lines.push(`export ${baseUrlVar}="${baseUrl}"`);
  }
  return `${lines.join('\n')}\n`;
}

// --- parseActiveEnv (inverse of renderActiveEnv) ----------------------------
// Pure: parses the ~/.hermit/aikey.env body back into { label, vars }. Reads the
// real file in runAikeyStatus to show what is currently claimed.
export function parseActiveEnv(content) {
  if (!content || typeof content !== 'string') return { label: null, vars: {} };
  const vars = {};
  let label = null;
  for (const line of content.split('\n')) {
    const match = line.match(/^export\s+([A-Za-z_][A-Za-z0-9_]*)="(.*)"\s*$/u);
    if (!match) continue;
    const [, name, value] = match;
    if (name === 'OPENHERMIT_ACTIVE_KEY') label = value;
    else vars[name] = value;
  }
  return { label, vars };
}

// --- buildShellHook (aikey ensure_shell_hook:1820) --------------------------
// Pure: returns { rcFile, block } for zsh/bash, or null for unsupported shells.
// `marker` makes the hook idempotent (installer skips if rcFile already has it).
const SHELL_HOOK_MARKER = '# openhermit shell hook';

export function buildShellHook({ shell, envPath, marker = SHELL_HOOK_MARKER }) {
  const sh = String(shell || '');
  const isZsh = sh.includes('zsh');
  const isBash = sh.includes('bash');
  if (!isZsh && !isBash) return null;

  const home = os.homedir();
  let rcFile;
  if (isZsh) {
    rcFile = path.join(home, '.zshrc');
  } else {
    // aikey: write to the first candidate that exists, else the first (.bashrc).
    const candidates = [path.join(home, '.bashrc'), path.join(home, '.bash_profile')];
    rcFile = candidates.find((c) => existsSync(c)) || candidates[0];
  }

  const block = isZsh
    ? `\n${marker}\n_openhermit_precmd() { [[ -f ${envPath} ]] && source ${envPath}; }\nprecmd_functions+=(_openhermit_precmd)\n`
    : `\n${marker}\nPROMPT_COMMAND='[[ -f ${envPath} ]] && source ${envPath}'\n`;

  return { rcFile, block };
}

// --- Key source: server (real, not-yet-supported) → mock fallback ------------

// Expected server response: { displayName?, providers: { <code|alias>: { apiKey, baseUrl? } } }.
function normalizeBundle(raw) {
  if (!raw || typeof raw !== 'object') return null;
  const providers = {};
  for (const [code, creds] of Object.entries(raw.providers || {})) {
    const canonical = canonicalProvider(code);
    if (!canonical) continue;
    const apiKey = creds && typeof creds === 'object' ? String(creds.apiKey || '').trim() : '';
    if (!apiKey) continue;
    const baseUrl = String(creds?.baseUrl || '').trim();
    providers[canonical] = baseUrl ? { apiKey, baseUrl } : { apiKey };
  }
  if (Object.keys(providers).length === 0) return null;
  return { displayName: raw.displayName || 'agentcli', providers };
}

// Local mock: ~/.hermit/aikey-mock.json. Lets ops drop real keys today so the
// full distribute→shell-hook→harness pipeline is exercised end-to-end.
export function readMockBundle({ home }) {
  const mockPath = path.join(home, 'aikey-mock.json');
  let raw;
  try {
    raw = JSON.parse(readFileSync(mockPath, 'utf-8'));
  } catch {
    return null;
  }
  return normalizeBundle(raw);
}

// GET <base>/api/v1/hermit/ai-key with the logged-in bearer token. Returns null
// on any failure (not logged in / 404 / network) so the caller falls back to mock.
async function fetchServerBundle() {
  const ctx = await resolveAuthedServerContext();
  if (!ctx) return null;
  const res = await fetch(`${ctx.baseUrl}/api/v1/hermit/ai-key`, {
    headers: { Authorization: `Bearer ${ctx.token}`, Accept: 'application/json' },
    signal: AbortSignal.timeout(8_000),
  });
  if (!res.ok) return null;
  const body = await res.json().catch(() => null);
  return normalizeBundle(body);
}

// Tries the server endpoint first; on any failure degrades to the local mock.
// Returns { source, displayName, providers } or null when neither is available.
export async function resolveApiKeyBundle({ home }) {
  try {
    const server = await fetchServerBundle();
    if (server) return { source: 'server', ...server };
  } catch {
    // Server not supported yet (or transient failure) — fall through to mock.
  }
  const mock = readMockBundle({ home });
  return mock ? { source: 'mock', ...mock } : null;
}

// --- Shell-hook installer (IO; mirrors aikey ensure_shell_hook side effects) --
// Returns a short human status string. Idempotent via the marker. Prompts [Y/n]
// in an interactive terminal (never in --json mode); honors --no-hook and
// OPENHERMIT_NO_HOOK=1.
async function installShellHook({ noHook, envPath }) {
  if (noHook || process.env.OPENHERMIT_NO_HOOK === '1') {
    return `已跳过 shell hook（--no-hook）。手动生效请执行：source ${envPath}`;
  }
  const hook = buildShellHook({ shell: process.env.SHELL || '', envPath });
  if (!hook) {
    return `未识别 SHELL，请手动添加到 shell 配置：source ${envPath}`;
  }
  let existing = '';
  try {
    existing = readFileSync(hook.rcFile, 'utf-8');
  } catch {
    // rcFile may not exist yet — append will create it.
  }
  if (existing.includes(SHELL_HOOK_MARKER)) {
    return `shell hook 已安装于 ${hook.rcFile}（无需重复安装）`;
  }
  if (isInteractiveCli() && !jsonRequested) {
    const rl = createPromptInterface();
    let answer = 'y';
    try {
      answer = await rl.question(`将向 ${hook.rcFile} 写入 precmd hook，是否继续？ [Y/n] `);
    } finally {
      rl.close();
    }
    if (['n', 'no'].includes(answer.trim().toLowerCase())) {
      return `已跳过 shell hook。手动生效请执行：source ${envPath}`;
    }
  }
  appendFileSync(hook.rcFile, hook.block);
  return `shell hook 已写入 ${hook.rcFile}`;
}

// --- Command entry -----------------------------------------------------------
// `exitOnDone` mirrors runAuthLogin/printServicesStatus: the CLI subcommand path
// exits on completion, the interactive-menu path passes false so control returns
// to the menu loop.
export async function runAikey({ noHook = false, exitOnDone = true } = {}) {
  const home = hermitHome;
  const envPath = path.join(home, 'aikey.env');
  const bundle = await resolveApiKeyBundle({ home });

  if (!bundle) {
    const rows = [
      ['状态', '无可认领的 aikey', 'error'],
      ['服务端', '尚未支持（规划中）', 'warn'],
      ['本地 mock', path.join(home, 'aikey-mock.json'), 'info'],
    ];
    if (jsonRequested) {
      printJson({ ok: false, command: 'aikey', reason: 'no-key-source' }, 1);
    }
    printCliRows('认领 aikey', rows, `先创建本地 mock：echo '{"displayName":"demo","providers":{"anthropic":{"apiKey":"sk-…","baseUrl":"https://api.anthropic.com"}}}' > ${path.join(home, 'aikey-mock.json')}，再重跑 agentcli aikey。`);
    if (exitOnDone) process.exit(1);
    return { ok: false };
  }

  mkdirSync(home, { recursive: true });
  writeFileSync(envPath, renderActiveEnv(bundle), { mode: 0o600 });

  const providerRows = Object.entries(bundle.providers).map(([code, creds]) => {
    const [apiKeyVar] = providerEnvVars(code);
    const masked = maskKey(creds.apiKey);
    return [apiKeyVar, creds.baseUrl ? `${masked}  →  ${creds.baseUrl}` : masked, 'ok'];
  });

  const hookStatus = await installShellHook({ noHook, envPath });
  const sourceLabel = bundle.source === 'server' ? `服务端 (${resolveConversationUploadBaseUrl()})` : `本地 mock (${path.join(home, 'aikey-mock.json')})`;

  if (jsonRequested) {
    printJson({
      ok: true,
      command: 'aikey',
      source: bundle.source,
      envPath,
      providers: Object.keys(bundle.providers),
      hook: hookStatus,
    });
  }

  printCliRows('认领 aikey', [
    ['来源', sourceLabel, 'info'],
    ...providerRows,
    ['写入', envPath, 'ok'],
    ['shell', hookStatus, 'ok'],
  ], `新开一个终端即可生效，或立即执行：source ${envPath}`);

  const result = { ok: true, source: bundle.source, envPath, providers: Object.keys(bundle.providers) };
  if (exitOnDone) process.exit(0);
  return result;
}

// --- 查看状态: read ~/.hermit/aikey.env + shell-hook presence, render a panel -
export async function runAikeyStatus({ exitOnDone = true } = {}) {
  const home = hermitHome;
  const envPath = path.join(home, 'aikey.env');
  let content = '';
  let exists = false;
  try {
    content = readFileSync(envPath, 'utf-8');
    exists = true;
  } catch {
    exists = false;
  }

  const hook = buildShellHook({ shell: process.env.SHELL || '', envPath });
  let hookInstalled = false;
  if (hook) {
    try {
      hookInstalled = readFileSync(hook.rcFile, 'utf-8').includes(SHELL_HOOK_MARKER);
    } catch {
      hookInstalled = false;
    }
  }

  if (!exists) {
    if (jsonRequested) printJson({ ok: true, command: 'aikey-status', claimed: false, envPath });
    printCliRows('aikey 状态', [
      ['状态', '未认领', 'warn'],
      ['文件', envPath, 'info'],
    ], '先选择「认领」或运行 agentcli aikey，认领后这里会显示已下发的 key。');
    if (exitOnDone) process.exit(0);
    return { ok: true, claimed: false };
  }

  const { label, vars } = parseActiveEnv(content);
  const keyNames = Object.keys(vars).filter((name) => name.endsWith('_API_KEY'));
  const hookText = !hook ? '未识别 SHELL' : hookInstalled ? `已安装 (${hook.rcFile})` : '未安装';

  if (jsonRequested) {
    printJson({ ok: true, command: 'aikey-status', claimed: true, label, providers: keyNames, envPath, hookInstalled });
  }
  printCliRows('aikey 状态', [
    ['状态', '已认领', 'ok'],
    ['身份', label || '—', 'info'],
    ...keyNames.map((name) => [name, maskKey(vars[name]), 'ok']),
    ['文件', envPath, 'info'],
    ['shell hook', hookText, hookInstalled ? 'ok' : 'warn'],
  ], hookInstalled
    ? '新终端会自动加载已认领的 key。'
    : `未装 shell hook：新终端需手动 source ${envPath}（或在「认领」时安装）。`);
  if (exitOnDone) process.exit(0);
  return { ok: true, claimed: true, label, providers: keyNames };
}

// --- applyToConfigs: write a claimed gateway key into the LOCAL runtime configs --
//
// The other half of token distribution: provision/claim lands a one-time key in
// memory; this writes it into the configs the local Codex / Claude actually read,
// so the user's machine talks to the gateway without per-shell env exports.
//
// All three files are edited SURGICALLY (no full-config resets):
//   • ~/.claude/settings.json — deep-merge env (AUTH_TOKEN/BASE_URL/MODEL),
//     preserve every other top-level key + existing env key.
//   • ~/.codex/auth.json      — overwrite OPENAI_API_KEY only.
//   • ~/.codex/config.toml    — section-aware edit: rewrite top-level model +
//     model_provider and the [model_providers.<name>] block, leaving every other
//     section ([projects.*] included) byte-for-byte intact. No TOML dependency —
//     same line/section discipline runtime.mjs uses for its migrations.
// Each pre-existing file is backed up to *.hermit-bak first; writes are atomic
// (temp + rename + chmod 0o600), mirroring auth.mjs::writeOpenHermitAuthStore.

const CLAUDE_DIR = '.claude';
const CODEX_DIR = '.codex';
const DEFAULT_PROVIDER_NAME = 'hermit';

function backupFile(filePath, enabled) {
  if (!enabled || !existsSync(filePath)) return null;
  const bak = `${filePath}.hermit-bak`;
  copyFileSync(filePath, bak);
  return bak;
}

// Atomic write shared by JSON + text targets. mode 0o600 because these files hold
// a live API key.
function atomicWrite(filePath, content, { backup = true } = {}) {
  const backupPath = backupFile(filePath, backup);
  mkdirSync(path.dirname(filePath), { recursive: true });
  const tempPath = `${filePath}.${process.pid}.tmp`;
  writeFileSync(tempPath, content, { encoding: 'utf-8', mode: 0o600 });
  chmodBestEffort(tempPath, 0o600);
  renameSync(tempPath, filePath);
  chmodBestEffort(filePath, 0o600);
  return backupPath;
}

function atomicWriteJson(filePath, value, opts) {
  return atomicWrite(filePath, `${JSON.stringify(value, null, 2)}\n`, opts);
}

function tomlQuoted(value) {
  return `"${escapeTomlPath(String(value ?? ''))}"`;
}

// Split raw TOML into the leading top-level key/value lines (before the first
// [section]) and a list of { header, body: [lines] }. Everything outside the keys
// we explicitly rewrite is returned verbatim, so [projects.*] and friends survive.
function splitTomlSections(raw) {
  const lines = (raw || '').split('\n');
  const head = [];
  const sections = [];
  let current = head;
  for (const line of lines) {
    if (/^\s*\[[^\]]+\]\s*$/.test(line)) {
      current = [];
      sections.push({ header: line, body: current });
    } else {
      current.push(line);
    }
  }
  return { head, sections };
}

function joinTomlSections(head, sections) {
  const out = [...head];
  for (const section of sections) {
    out.push(section.header);
    out.push(...section.body);
  }
  return out.join('\n');
}

function readTopLevelTomlValue(head, key) {
  const re = new RegExp(`^\\s*${key}\\s*=\\s*(.+?)\\s*$`);
  for (const line of head) {
    const match = line.match(re);
    if (!match) continue;
    const raw = match[1].trim();
    if ((raw.startsWith('"') && raw.endsWith('"')) || (raw.startsWith("'") && raw.endsWith("'"))) {
      return raw.slice(1, -1);
    }
    return raw;
  }
  return null;
}

// Replace a top-level `key = ...` line in place, or append it to the head block
// when absent (trimming the blank line that typically separates head from the
// first section so the appended line lands cleanly).
function setTopLevelTomlKey(head, key, value) {
  const re = new RegExp(`^\\s*${key}\\s*=`);
  const idx = head.findIndex((line) => re.test(line));
  const line = `${key} = ${tomlQuoted(value)}`;
  if (idx >= 0) {
    head[idx] = line;
    return;
  }
  while (head.length && head[head.length - 1].trim() === '') head.pop();
  head.push(line);
}

// Replace-or-insert the [model_providers.<name>] section. When inserting, it goes
// right after the last existing [model_providers.*] block (keeps related sections
// together); else appended at the end.
function setModelProviderSection(sections, name, { endpoint, wireApi }) {
  const header = `[model_providers.${name.replace(/[\]\\]/g, '')}]`;
  const body = [
    `name = ${tomlQuoted(name)}`,
    `base_url = ${tomlQuoted(endpoint)}`,
    `wire_api = ${tomlQuoted(wireApi)}`,
    'requires_openai_auth = true',
    '',
  ];
  const idx = sections.findIndex((section) => section.header.trim() === header);
  if (idx >= 0) {
    sections[idx].body = body;
    return;
  }
  let insertAt = sections.length;
  for (let i = 0; i < sections.length; i += 1) {
    if (/^\s*\[model_providers\./.test(sections[i].header)) insertAt = i + 1;
  }
  sections.splice(insertAt, 0, { header, body });
}

function applyClaudeConfig({ key, endpoint, model, home, backup }) {
  const filePath = path.join(home, CLAUDE_DIR, 'settings.json');
  const { value } = safeReadJson(filePath);
  const settings = value && typeof value === 'object' && !Array.isArray(value) ? value : {};
  const existingEnv = settings.env && typeof settings.env === 'object' && !Array.isArray(settings.env) ? settings.env : {};
  const env = {
    ...existingEnv,
    ANTHROPIC_BASE_URL: endpoint,
    // AUTH_TOKEN (not API_KEY): Claude Code honors ANTHROPIC_AUTH_TOKEN for a
    // custom-gateway/base-url setup, matching the user's existing config style.
    ANTHROPIC_AUTH_TOKEN: key,
  };
  if (model) env.ANTHROPIC_MODEL = model;
  const backupPath = atomicWriteJson(filePath, { ...settings, env }, { backup });
  return { runtime: 'claude', path: filePath, changed: true, backupPath };
}

function applyCodexAuth({ key, home, backup }) {
  const filePath = path.join(home, CODEX_DIR, 'auth.json');
  const { value } = safeReadJson(filePath);
  const auth = value && typeof value === 'object' && !Array.isArray(value) ? value : {};
  const backupPath = atomicWriteJson(filePath, { ...auth, OPENAI_API_KEY: key }, { backup });
  return { runtime: 'codex-auth', path: filePath, changed: true, backupPath };
}

function applyCodexConfig({ endpoint, model, wireApi, home, backup, providerName }) {
  const filePath = path.join(home, CODEX_DIR, 'config.toml');
  let raw = '';
  try {
    raw = readFileSync(filePath, 'utf-8');
  } catch {
    raw = '';
  }
  const { head, sections } = splitTomlSections(raw);
  // Reuse the user's existing model_provider name if they already had one; this
  // rewrites their configured provider in place rather than orphaning it.
  const name = readTopLevelTomlValue(head, 'model_provider') || providerName || DEFAULT_PROVIDER_NAME;
  setTopLevelTomlKey(head, 'model_provider', name);
  if (model) setTopLevelTomlKey(head, 'model', model);
  setModelProviderSection(sections, name, { endpoint, wireApi });
  const backupPath = atomicWrite(filePath, joinTomlSections(head, sections), { backup });
  return { runtime: 'codex-config', path: filePath, changed: true, backupPath, provider: name };
}

// runtimes: which local runtimes to configure. Defaults to both Codex + Claude
// (the user-confirmed scope). `home` defaults to os.homedir() but is overridable
// for tests so they never touch the developer's real ~/.claude / ~/.codex.
export function applyToConfigs({
  key,
  endpoint,
  model,
  wireApi = 'chat',
  runtimes = ['codex', 'claude'],
  backup = true,
  home = os.homedir(),
} = {}) {
  if (!key) throw new Error('applyToConfigs: 缺少 key');
  if (!endpoint) throw new Error('applyToConfigs: 缺少 endpoint');
  const wanted = new Set(runtimes);
  const results = [];
  if (wanted.has('claude')) results.push(applyClaudeConfig({ key, endpoint, model, home, backup }));
  if (wanted.has('codex')) {
    results.push(applyCodexAuth({ key, home, backup }));
    results.push(applyCodexConfig({ endpoint, model, wireApi, home, backup }));
  }
  return { ok: true, runtimes: results };
}

export function maskKey(key) {
  const k = String(key || '');
  if (k.length <= 8) return '****';
  return `${k.slice(0, 4)}…${k.slice(-4)}`;
}

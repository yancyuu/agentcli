function backupFile(filePath, enabled) {
  if (!enabled || !existsSync(filePath)) return null;
  const bak = `${filePath}.hermit-bak`;
  copyFileSync(filePath, bak);
  return bak;
}

// Atomic write shared by JSON + text targets. mode 0o600 because these files hold
// a live API key.
function atomicWrite(filePath, content, { backup = true } = {}) {
  const backupPath = backupFile(filePath, backup);
  atomicWriteFile(filePath, content, { mode: 0o600 });
  return backupPath;
}

function atomicWriteJson(filePath, value, opts) {
  return atomicWrite(filePath, `${JSON.stringify(value, null, 2)}\n`, opts);
}

function tomlQuoted(value) {
  return `"${escapeTomlPath(String(value ?? ''))}"`;
}

// TOML bare keys allow A–Z a–z 0–9 _ -. Sanitize provider names so the
// [model_providers.<name>] header always lines up with the quoted
// `model_provider = "<name>"` value and Codex's parser accepts both.
function tomlBareKey(name) {
  return String(name ?? '').replace(/[^A-Za-z0-9_-]/g, '-');
}

// Split raw TOML into the leading top-level key/value lines (before the first
// [section]) and a list of { header, body: [lines] }. Everything outside the keys
// we explicitly rewrite is returned verbatim, so [projects.*] and friends survive.
function splitTomlSections(raw) {
  const lines = (raw || '').split('\n');
  const head = [];
  const sections = [];
  let current = head;
  for (const line of lines) {
    if (/^\s*\[[^\]]+\]\s*$/.test(line)) {
      current = [];
      sections.push({ header: line, body: current });
    } else {
      current.push(line);
    }
  }
  return { head, sections };
}

function joinTomlSections(head, sections) {
  const out = [...head];
  for (const section of sections) {
    out.push(section.header);
    out.push(...section.body);
  }
  return out.join('\n');
}

function readTopLevelTomlValue(head, key) {
  const re = new RegExp(`^\\s*${key}\\s*=\\s*(.+?)\\s*$`);
  for (const line of head) {
    const match = line.match(re);
    if (!match) continue;
    const raw = match[1].trim();
    if ((raw.startsWith('"') && raw.endsWith('"')) || (raw.startsWith("'") && raw.endsWith("'"))) {
      return raw.slice(1, -1);
    }
    return raw;
  }
  return null;
}

// Replace a top-level `key = ...` line in place, or append it to the head block
// when absent (trimming the blank line that typically separates head from the
// first section so the appended line lands cleanly).
function setTopLevelTomlKey(head, key, value) {
  const re = new RegExp(`^\\s*${key}\\s*=`);
  const idx = head.findIndex((line) => re.test(line));
  const line = `${key} = ${tomlQuoted(value)}`;
  if (idx >= 0) {
    head[idx] = line;
    return;
  }
  while (head.length && head[head.length - 1].trim() === '') head.pop();
  head.push(line);
}

// Replace-or-insert the [model_providers.<name>] section. When inserting, it goes
// right after the last existing [model_providers.*] block (keeps related sections
// together); else appended at the end.
function setModelProviderSection(sections, name, { endpoint, wireApi }) {
  const header = `[model_providers.${tomlBareKey(name)}]`;
  const body = [
    `name = ${tomlQuoted(name)}`,
    `base_url = ${tomlQuoted(endpoint)}`,
    `wire_api = ${tomlQuoted(wireApi)}`,
    'requires_openai_auth = true',
    '',
  ];
  const idx = sections.findIndex((section) => section.header.trim() === header);
  if (idx >= 0) {
    sections[idx].body = body;
    return;
  }
  let insertAt = sections.length;
  for (let i = 0; i < sections.length; i += 1) {
    if (/^\s*\[model_providers\./.test(sections[i].header)) insertAt = i + 1;
  }
  sections.splice(insertAt, 0, { header, body });
}

function applyClaudeConfig({ key, endpoint, tierModels, home, backup }) {
  const filePath = path.join(home, CLAUDE_DIR, 'settings.json');
  const { value } = safeReadJson(filePath);
  const settings = value && typeof value === 'object' && !Array.isArray(value) ? value : {};
  const existingEnv = settings.env && typeof settings.env === 'object' && !Array.isArray(settings.env) ? settings.env : {};
  const env = {
    ...existingEnv,
    ANTHROPIC_BASE_URL: endpoint,
    // AUTH_TOKEN (not API_KEY): Claude Code honors ANTHROPIC_AUTH_TOKEN for a
    // custom-gateway/base-url setup, matching the user's existing config style.
    ANTHROPIC_AUTH_TOKEN: key,
  };
  // Tier vars (ANTHROPIC_DEFAULT_*_MODEL) replace the old single ANTHROPIC_MODEL.
  // Two model sources is technical debt; tier vars cover all three tiers.
  if (tierModels?.haiku) env.ANTHROPIC_DEFAULT_HAIKU_MODEL = tierModels.haiku;
  if (tierModels?.sonnet) env.ANTHROPIC_DEFAULT_SONNET_MODEL = tierModels.sonnet;
  if (tierModels?.opus) env.ANTHROPIC_DEFAULT_OPUS_MODEL = tierModels.opus;
  const backupPath = atomicWriteJson(filePath, { ...settings, env }, { backup });
  return { runtime: 'claude', path: filePath, changed: true, backupPath };
}

function applyCodexAuth({ key, home, backup }) {
  const filePath = path.join(home, CODEX_DIR, 'auth.json');
  // FULL REPLACE, not merge. A prior `codex login` (OAuth) leaves a `tokens`
  // object (id_token / access_token / refresh_token) plus other keys here, and
  // Codex prefers those OAuth tokens over OPENAI_API_KEY — so a merged write left
  // the claimed gateway key ignored (and the stale OAuth login winning). Overwrite
  // the whole file with just the API key. The snapshot in configEnvBackup.mjs
  // still owns the one-click restore to the pre-claim state.
  const backupPath = atomicWriteJson(filePath, { OPENAI_API_KEY: key }, { backup });
  return { runtime: 'codex-auth', path: filePath, changed: true, backupPath };
}

function applyCodexConfig({ endpoint, model, wireApi, home, backup, providerName }) {
  const filePath = path.join(home, CODEX_DIR, 'config.toml');
  let raw = '';
  try {
    raw = readFileSync(filePath, 'utf-8');
  } catch {
    raw = '';
  }
  const { head, sections } = splitTomlSections(raw);
  // Reuse the user's existing model_provider name if they already had one; this
  // rewrites their configured provider in place rather than orphaning it.
  const name = readTopLevelTomlValue(head, 'model_provider') || providerName || DEFAULT_PROVIDER_NAME;
  setTopLevelTomlKey(head, 'model_provider', name);
  if (model) setTopLevelTomlKey(head, 'model', model);
  setModelProviderSection(sections, name, { endpoint, wireApi });
  const backupPath = atomicWrite(filePath, joinTomlSections(head, sections), { backup });
  return { runtime: 'codex-config', path: filePath, changed: true, backupPath, provider: name };
}

// runtimes: which local runtimes to configure. Defaults to both Codex + Claude
// (the user-confirmed scope). `home` defaults to os.homedir() but is overridable
// for tests so they never touch the developer's real ~/.claude / ~/.codex.
export function applyToConfigs({
  key,
  endpoint,
  model,
  tierModels,
  wireApi = DEFAULT_WIRE_API,
  runtimes = ['codex', 'claude'],
  backup = true,
  home = os.homedir(),
} = {}) {
  if (!key) throw new Error('applyToConfigs: 缺少 key');
  if (!endpoint) throw new Error('applyToConfigs: 缺少 endpoint');
  const wanted = new Set(runtimes);
  const results = [];
  if (wanted.has('claude')) results.push(applyClaudeConfig({ key, endpoint, tierModels, home, backup }));
  if (wanted.has('codex')) {
    results.push(applyCodexAuth({ key, home, backup }));
    results.push(applyCodexConfig({ endpoint, model, wireApi, home, backup }));
  }
  return { ok: true, runtimes: results };
}

// Pi (~/.pi/agent/models.json) — reuses the codex contract (cpamc-openai +
// openai-responses). Upserts a single `agentcli` provider carrying the claimed
// key + endpoint + model; the user's other pi providers are left untouched.
// Atomic (tmp+rename via atomicWriteFile). models.json reloads on `/model`.
export function applyPiConfig({ key, baseUrl, model, home = os.homedir() } = {}) {
  if (!key) throw new Error('applyPiConfig: 缺少 key');
  if (!baseUrl) throw new Error('applyPiConfig: 缺少 baseUrl');
  const piDir = path.join(home, '.pi', 'agent');
  const modelsFile = path.join(piDir, 'models.json');
  let existing = { providers: {} };
  try {
    const parsed = JSON.parse(readFileSync(modelsFile, 'utf8'));
    if (parsed && typeof parsed === 'object' && parsed.providers && typeof parsed.providers === 'object') {
      existing = parsed;
    }
  } catch {
    // missing or corrupt — start fresh (dir may not exist on a clean machine)
  }
  const modelId = String(model || 'gpt-5.6-sol').trim();
  existing.providers = existing.providers || {};
  existing.providers.agentcli = {
    baseUrl,
    api: 'openai-responses',
    apiKey: key,
    authHeader: true,
    models: [
      {
        id: modelId,
        name: `${modelId} (agentcli)`,
        reasoning: true,
        input: ['text', 'image'],
        contextWindow: 272000,
        maxTokens: 16384,
      },
    ],
  };
  mkdirSync(piDir, { recursive: true });
  atomicWriteFile(modelsFile, `${JSON.stringify(existing, null, 2)}\n`);
  return { provider: 'agentcli', path: modelsFile, baseUrl, model: modelId };
}

// --- Claim orchestration layer ------------------------------------------------
// Sits above applyToConfigs: turns a claimed token-pool secret into the correct
// per-runtime endpoint + writes. configEnvBackup.mjs owns the original snapshot,
// so these always write with backup:false (no stray .hermit-bak files).

// Claude Code base_url — the v3 receipt's ready-to-use anthropic endpoint.
// Claude base_url — prefer the authoritative runtime_profiles.claude.base_url
// (token-distribution-v3 contract 74abdbf), fall back to endpoints.anthropic.
export function resolveClaudeBaseUrl(secret) {
  const rp = secret?.runtimeProfiles?.claude?.base_url;
  if (rp) return String(rp).trim();
  return String(secret?.endpoints?.anthropic || '').trim();
}

// Codex base_url — prefer runtime_profiles.codex.base_url, fall back to
// endpoints.openai. OpenAI-compatible APIs require the /v1 prefix.
export function resolveCodexBaseUrl(secret) {
  const rp = String(secret?.runtimeProfiles?.codex?.base_url || '')
    .trim()
    .replace(/\/+$/, '');
  const endpoint = rp || String(secret?.endpoints?.openai || '').trim().replace(/\/+$/, '');
  if (!endpoint) return '';
  return endpoint.endsWith('/v1') ? endpoint : `${endpoint}/v1`;
}

// Apply a claimed secret to ONLY the chosen runtimes. Claude gets the gateway
// endpoint with tier model vars (auto-mapped from receipt modelIds); Codex gets
// the resolved proxy base_url + model + wire_api. backup:false — configEnvBackup.mjs
// owns the snapshot/restore story. choices: { model?, wireApi? }.
// --- Claimed-secret usability guard -----------------------------------------
// The token-pool gateway returns non-functional placeholder values when it has
// no real key to issue — e.g. key "sk-homeless" and base_url "https://gw/cpaopen"
// (host "gw" is not a routable domain). Writing those overwrites the user's
// working config with values that can never connect. validateClaimedSecret()
// detects them so a "successful" claim that only yielded sentinels is treated as
// a failure and the config is left untouched ("失败就是失败，失败就不要改配置").

// Hostnames that are clearly not usable data-plane endpoints: a bare label with
// no dot and no IP/localhost form (e.g. "gw") can never resolve publicly.
export function isRoutableHost(host) {
  const h = String(host || '').toLowerCase().trim();
  if (!h || h === 'localhost' || h === '127.0.0.1') return true;
  if (/^\[?[0-9a-fA-F:]+\]?$/.test(h) && h.includes(':')) return true; // IPv6
  if (/^\d{1,3}(\.\d{1,3}){3}$/.test(h)) return true;                  // IPv4
  return /^[a-z0-9.-]+\.[a-z]{2,}$/i.test(h);                          // FQDN: needs a dot + TLD
}

// Known placeholder/sentinel key tokens the gateway emits instead of a real key.
const PLACEHOLDER_KEY = /^(sk-)?(homeless|placeholder|test|example|demo|sample|none|null|undefined|empty|changeme|your[-_]?key|xxx+|todo|fake|dummy|invalid|unavailable|na|n\/a)\b/i;

export function validateClaimedSecret(secret) {
  const key = String(secret?.key || secret?.api_key || secret?.plaintext_key || '').trim();
  if (!key) return { ok: false, reason: '服务端未返回 key' };
  if (PLACEHOLDER_KEY.test(key)) return { ok: false, reason: `返回的 key 是占位值（${key}），不可用` };
  const endpoints = secret?.endpoints || {};
  for (const [runtime, url] of Object.entries(endpoints)) {
    const u = String(url || '').trim();
    if (!u) continue;
    let host = '';
    try { host = new URL(u).hostname; } catch { host = ''; }
    if (!isRoutableHost(host)) return { ok: false, reason: `返回的 ${runtime} endpoint 不可路由（${u}），不可用` };
  }
  return { ok: true };
}

export function applyClaimedSecret({ secret, choices = {}, runtimes = ['claude', 'codex'], home } = {}) {
  const key = String(secret?.key || secret?.api_key || secret?.plaintext_key || '').trim();
  if (!key) throw new Error('applyClaimedSecret: 缺少 key');
  const claimCheck = validateClaimedSecret(secret);
  if (!claimCheck.ok) throw new Error(`applyClaimedSecret: ${claimCheck.reason}`);
  const wanted = Array.isArray(runtimes) ? runtimes : [runtimes];
  const results = [];
  const endpoints = {};
  // The user picks ONE model for Codex; Claude tier vars all use that same model.
  // No version-based splitting — one model, three tiers.
  const singleModel = choices?.model || (Array.isArray(secret?.modelIds) ? secret.modelIds[0] : null);
  const tierModels = singleModel ? { haiku: singleModel, sonnet: singleModel, opus: singleModel } : {};
  if (wanted.includes('claude')) {
    const claudeEndpoint = resolveClaudeBaseUrl(secret);
    endpoints.claude = claudeEndpoint;
    results.push(
      ...applyToConfigs({
        key,
        endpoint: claudeEndpoint,
        tierModels,
        runtimes: ['claude'],
        backup: false,
        ...(home ? { home } : {}),
      }).runtimes,
    );
  }
  if (wanted.includes('codex')) {
    const wireApi = choices.wireApi || DEFAULT_WIRE_API;
    const codexEndpoint = resolveCodexBaseUrl(secret);
    endpoints.codex = codexEndpoint;
    results.push(
      ...applyToConfigs({
        key,
        endpoint: codexEndpoint,
        model: choices.model,
        wireApi,
        runtimes: ['codex'],
        backup: false,
        ...(home ? { home } : {}),
      }).runtimes,
    );
  }
  if (wanted.includes('pi')) {
    // Pi reuses the codex (cpamc-openai + responses) contract: openai-responses
    // API, baseUrl = codex base + /v1. Upserts an `agentcli` provider into
    // ~/.pi/agent/models.json; the user's other pi providers stay untouched.
    // Best-effort: a pi write failure must NOT roll back claude/codex already done.
    try {
      const piBaseUrl = resolveCodexBaseUrl(secret);
      if (piBaseUrl) {
        const pi = applyPiConfig({
          key,
          baseUrl: piBaseUrl,
          model: choices.model,
          ...(home ? { home } : {}),
        });
        results.push({ runtime: 'pi', ok: true, ...pi });
        endpoints.pi = piBaseUrl;
      }
    } catch (err) {
      results.push({ runtime: 'pi', ok: false, error: err?.message || String(err) });
    }
  }
  // Env-file injection: write ~/.hermit/aikey.env so external agents can source
  // the key + base_url directly (no plaintext in the manual — it just names the vars).
  // The env file carries ANTHROPIC_API_KEY/OPENAI_API_KEY + their BASE_URLs;
  // tier model vars (ANTHROPIC_DEFAULT_*_MODEL) are Claude-Code-specific and live
  // in settings.json env block — not in the sourced env file.
  const envHome = home || hermitHome;
  writeAikeyEnv({
    bundle: {
      displayName: 'agentcli',
      providers: {
        anthropic: { apiKey: key, baseUrl: endpoints.claude || '' },
        openai: { apiKey: key, baseUrl: endpoints.codex || '' },
      },
    },
    home: envHome,
  });
  return { ok: true, runtimes: results, endpoints, tierModels };
}

// --- System environment injection (one-shot, at claim time) -------------------
// See the file header: this is NOT the removed per-prompt hook. It writes once
// per claim, and every surface is best-effort (reported, never thrown) so an
// env-write failure can never fail a claim whose config files already landed.
//
//   • macOS/Linux — an idempotent marked block in the primary shell rc file
//     (~/.zshrc on macOS, ~/.bashrc on Linux; the secondary rc only when the
//     user already has one — don't scatter new dotfiles), so every NEW shell
//     exports the key. macOS additionally gets `launchctl setenv` so
//     already-running GUI apps pick it up.
//   • Windows — HKCU user environment via [Environment]::SetEnvironmentVariable
//     (survives reboot; new terminals see it). Secrets travel over stdin, never
//     in argv (argv is world-visible via `ps`).

const ENV_BLOCK_BEGIN = '# >>> hermit aikey >>>';
const ENV_BLOCK_END = '# <<< hermit aikey <<<';

// Escape for POSIX double-quoted strings: " \ $ and backtick stay special there.
function shellEscapeDoubleQuoted(value) {
  return String(value).replace(/(["\\$`])/g, '\\$1');
}

export function renderShellEnvBlock(vars) {
  const lines = [
    ENV_BLOCK_BEGIN,
    '# auto-generated by agentcli 认领 — manual edits are overwritten on the next claim',
  ];
  for (const [name, raw] of Object.entries(vars || {})) {
    const value = String(raw ?? '').trim();
    if (!value) continue;
    lines.push(`export ${name}="${shellEscapeDoubleQuoted(value)}"`);
  }
  lines.push(ENV_BLOCK_END);
  return `${lines.join('\n')}\n`;
}

// Replace the hermit block in `content` (markers inclusive), or append it when
// no well-formed block exists. Content outside the markers is kept verbatim.
export function upsertMarkedBlock(content, block) {
  const body = String(content || '');
  const lines = body.split('\n');
  const beginIdx = lines.findIndex((line) => line.trim() === ENV_BLOCK_BEGIN);
  const endIdx = beginIdx >= 0
    ? lines.findIndex((line, idx) => idx > beginIdx && line.trim() === ENV_BLOCK_END)
    : -1;
  const blockLines = block.replace(/\n+$/, '').split('\n');
  if (beginIdx >= 0 && endIdx > beginIdx) {
    lines.splice(beginIdx, endIdx - beginIdx + 1, ...blockLines);
    return lines.join('\n');
  }
  const trimmed = body.replace(/\s+$/, '');
  return trimmed ? `${trimmed}\n\n${block}` : block;
}

// Primary rc is always written; the secondary only when it already exists.
function rcPlanFor(platform) {
  if (platform === 'darwin') return [{ name: '.zshrc', always: true }, { name: '.bashrc', always: false }];
  if (platform === 'linux') return [{ name: '.bashrc', always: true }, { name: '.zshrc', always: false }];
  return [];
}

export function applyShellRcEnv({ vars, home, plan }) {
  const block = renderShellEnvBlock(vars);
  const results = [];
  for (const { name, always } of plan || []) {
    const rcPath = path.join(home, name);
    try {
      if (!always && !existsSync(rcPath)) {
        results.push({ surface: rcPath, ok: true, skipped: true });
        continue;
      }
      let current = '';
      try {
        current = readFileSync(rcPath, 'utf-8');
      } catch {
        // New rc file — nothing to preserve, nothing to back up.
      }
      backupFile(rcPath, true); // no-op unless the file pre-exists
      atomicWriteFile(rcPath, upsertMarkedBlock(current, block), { mode: 0o600 });
      results.push({ surface: rcPath, ok: true });
    } catch (error) {
      results.push({ surface: rcPath, ok: false, message: String(error?.message || error) });
    }
  }
  return results;
}

function applyLaunchctlEnv({ vars, spawnImpl }) {
  const failures = [];
  for (const [name, value] of Object.entries(vars)) {
    try {
      const res = spawnImpl('launchctl', ['setenv', name, value], { encoding: 'utf-8' });
      if (res?.error) throw res.error;
      if (res?.status !== 0) throw new Error(String(res?.stderr || `exit ${res?.status}`).trim());
    } catch (error) {
      failures.push(`${name}: ${error?.message || error}`);
    }
  }
  return failures.length === 0
    ? { surface: 'launchctl', ok: true }
    : { surface: 'launchctl', ok: false, message: failures.join('; ') };
}

function applyWindowsUserEnv({ vars, spawnImpl }) {
  const ps = [
    '$ErrorActionPreference="Stop"',
    '$vars = [Console]::In.ReadToEnd() | ConvertFrom-Json',
    'foreach ($p in $vars.PSObject.Properties) { [Environment]::SetEnvironmentVariable($p.Name, $p.Value, [EnvironmentVariableTarget]::User) }',
  ].join('; ');
  try {
    const res = spawnImpl('powershell', ['-NoProfile', '-NonInteractive', '-Command', ps], {
      encoding: 'utf-8',
      shell: false,
      input: JSON.stringify(vars),
    });
    if (res?.error) throw res.error;
    if (res?.status !== 0) throw new Error(String(res?.stderr || `exit ${res?.status}`).trim());
    return { surface: 'registry-user-env', ok: true };
  } catch (error) {
    return { surface: 'registry-user-env', ok: false, message: String(error?.message || error) };
  }
}

function sanitizeEnvVars(vars) {
  const clean = {};
  for (const [name, raw] of Object.entries(vars || {})) {
    const value = String(raw ?? '').trim();
    if (value) clean[name] = value;
  }
  return clean;
}

export function applyToEnvironment({
  vars,
  home = os.homedir(),
  platform = process.platform,
  spawnImpl = spawnSync,
} = {}) {
  const clean = sanitizeEnvVars(vars);
  if (Object.keys(clean).length === 0) return { ok: true, results: [] };
  const results = [];
  if (platform === 'win32') {
    results.push(applyWindowsUserEnv({ vars: clean, spawnImpl }));
  } else {
    results.push(...applyShellRcEnv({ vars: clean, home, plan: rcPlanFor(platform) }));
    if (platform === 'darwin') results.push(applyLaunchctlEnv({ vars: clean, spawnImpl }));
  }
  return { ok: results.every((r) => r.ok), results };
}

// Vars for the token-pool claim path. Mirrors the settings.json choice: Claude
// gets ANTHROPIC_AUTH_TOKEN (NOT API_KEY — Claude Code honors AUTH_TOKEN for
// custom gateways, and exporting both makes it send conflicting auth headers),
// Codex gets OPENAI_API_KEY. Only the user's chosen runtimes are exported.
export function systemEnvVarsForClaim({ secret, endpoints = {}, runtimes = ['claude', 'codex'] } = {}) {
  const key = String(secret?.key || secret?.api_key || secret?.plaintext_key || '').trim();
  if (!key) return {};
  const wanted = Array.isArray(runtimes) ? runtimes : [runtimes];
  const vars = {};
  if (wanted.includes('claude')) {
    vars.ANTHROPIC_AUTH_TOKEN = key;
    if (endpoints.claude) vars.ANTHROPIC_BASE_URL = endpoints.claude;
  }
  if (wanted.includes('codex')) {
    vars.OPENAI_API_KEY = key;
    if (endpoints.codex) vars.OPENAI_BASE_URL = endpoints.codex;
  }
  return vars;
}

export function maskKey(key) {
  const k = String(key || '');
  if (k.length <= 8) return '****';
  return `${k.slice(0, 4)}…${k.slice(-4)}`;
}
